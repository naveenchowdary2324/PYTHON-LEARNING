#Logical Operators & MemberShip Operators & Identity Operators

#Logical Operators

a =10
b =20
print("And Operator", a<b and a<10)
print("Or Operator", a<b or a<10)
print("NOT Operator" , not b<10)

# MemberShip Operators

str1 = "My name is Naveen"
print("is" in str1)
print("Naga" in str1)
print("Naga" not in str1)

# Identity Operators

a1 =100
b1 = 300
print(a1 is b1)
print(a1 is not b1)
