#Control Flow Statements
from asyncio import Condition

# IF Condition
a = 100
b = 15
if a > b:
    print("a is greater than b")


# IF-Else Condition
x = 12
y = 32
if x == y:
    print("x is equal to y")
else:
    print("x is not equal to y")

# elseif condition

if x < y:
    print("x is less than y")
elif x > y:
    print("x is greater than y")
elif x == y:
    print("x is equal to y")
else:
    print("x is not equal to y")
