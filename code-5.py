#Comparision Operators


a = 100
b = 200

print("a==b",a==b) #Check if a is equal to b
print("a!=b",a!=b) #Check if a is not equal to b
print("a>b",a>b) #Check if a greater than b
print("a>=b",a>=b) #Check if a greater than or equal to b
print("a<b",a<b) #Check if a less than b
print("a<=b",a<=b) #Check if a less than or equal to b
