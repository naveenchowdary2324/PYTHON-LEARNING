# Arithmetic Operators & Assignment Operators

a = 10
b = 20
print(a+b) #addition
print(a-b) #Substraction
print(a*b) #Multiplication
print(a/b) #Division
print(a//b) #FloorDivision
print(a%b) #Modulus(Remainder)
print(a**b) #Power(Exponent)

# Assignment Operators

x = 6
y = 5
x += y #x = x+y
print(x)
x -= y #x = x-y
print(x)
x *= y #x = x*y
print(x)
x /= y #x = x/y
print(x)
x **= y #x = x**y
print(x)
